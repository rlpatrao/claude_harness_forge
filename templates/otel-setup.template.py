def instrument_app(app: FastAPI) -> FastAPI:
    """Call once at app startup to wire OTel into the FastAPI instance.

    Example::

        app = FastAPI(title="{{SERVICE_NAME}}")
        instrument_app(app)
    """
    # Auto-instrument routes (creates spans per endpoint)
    FastAPIInstrumentor.instrument_app(app)

    # Add metric-collection middleware
    app.add_middleware(OTelMiddleware)

    logger.info("otel_initialized", service=SERVICE_NAME, endpoint=OTLP_ENDPOINT)
    return app


