class OTelMiddleware(BaseHTTPMiddleware):
    """Captures per-request metrics and ensures trace context propagation."""

    async def dispatch(self, request: Request, call_next: Callable) -> Response:
        # Propagate incoming trace context (e.g. from an upstream gateway)
        ctx = extract(carrier=dict(request.headers))
        token = attach(ctx)

        method = request.method
        path = request.url.path
        start = time.perf_counter()

        try:
            response = await call_next(request)
        except Exception:
            error_counter.add(1, {"method": method, "path": path, "status": "500"})
            raise
        finally:
            elapsed = time.perf_counter() - start
            status = str(getattr(response, "status_code", 500))

            request_counter.add(1, {"method": method, "path": path, "status": status})
            request_latency.record(elapsed, {"method": method, "path": path})

            if int(status) >= 400:
                error_counter.add(
                    1, {"method": method, "path": path, "status": status}
                )

            detach(token)

        return response


