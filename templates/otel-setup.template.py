def traced(
    name: str | None = None,
    *,
    record_exception: bool = True,
    attributes: dict[str, str] | None = None,
) -> Callable[[F], F]:
    """Decorator that wraps a sync or async function in an OTel span.

    Usage::

        @traced("compute_embeddings")
        async def compute_embeddings(texts: list[str]) -> list[list[float]]:
            ...
    """

    def decorator(fn: F) -> F:
        span_name = name or fn.__qualname__

        if _is_coroutine_function(fn):

            @wraps(fn)
            async def _async_wrapper(*args: Any, **kwargs: Any) -> Any:
                with tracer.start_as_current_span(
                    span_name, attributes=attributes or {}
                ) as span:
                    try:
                        result = await fn(*args, **kwargs)
                    except Exception as exc:
                        span.set_status(StatusCode.ERROR, str(exc))
                        if record_exception:
                            span.record_exception(exc)
                        raise
                    return result

            return _async_wrapper  # type: ignore[return-value]

        @wraps(fn)
        def _sync_wrapper(*args: Any, **kwargs: Any) -> Any:
            with tracer.start_as_current_span(
                span_name, attributes=attributes or {}
            ) as span:
                try:
                    result = fn(*args, **kwargs)
                except Exception as exc:
                    span.set_status(StatusCode.ERROR, str(exc))
                    if record_exception:
                        span.record_exception(exc)
                    raise
                return result

        return _sync_wrapper  # type: ignore[return-value]

    return decorator


def _is_coroutine_function(fn: Callable) -> bool:
    import asyncio
    import inspect

    return asyncio.iscoroutinefunction(fn) or inspect.iscoroutinefunction(fn)


