class RAGPipeline:
    """End-to-end RAG pipeline wiring all services together."""

    def __init__(
        self,
        loader: DocumentLoader | None = None,
        chunker: ChunkingService | None = None,
        embedder: EmbeddingService | None = None,
        vector_store: VectorStoreClient | None = None,
        retriever: RetrievalService | None = None,
        reranker: RerankingService | None = None,
        generator: GenerationService | None = None,
    ) -> None:
        self.loader = loader or DocumentLoader()
        self.chunker = chunker or ChunkingService(strategy="fixed")
        self.embedder = embedder or EmbeddingService(provider="openai")
        self.vector_store = vector_store or create_vector_store()
        self.retriever = retriever or RetrievalService(self.vector_store, self.embedder)
        self.reranker = reranker or RerankingService(method="none")
        self.generator = generator or GenerationService()

    def ingest(self, source: str | Path) -> int:
        """Load, chunk, embed, and store documents from a file or directory.

        Returns the number of chunks stored.
        """
        documents = self.loader.load(source)
        chunks = self.chunker.chunk_documents(documents)
        embeddings = self.embedder.embed_chunks(chunks)

        ids = [c.chunk_id for c in chunks]
        metadatas = [c.metadata for c in chunks]
        texts = [c.text for c in chunks]

        self.vector_store.upsert(ids, embeddings, metadatas, texts)
        logger.info("Ingested %d chunks from %s", len(chunks), source)
        return len(chunks)

    def query(self, question: str, top_k: int = 5) -> GenerationResult:
        """Retrieve relevant context and generate an answer."""
        results = self.retriever.search(question, top_k=top_k * 2)
        reranked = self.reranker.rerank(question, results, top_k=top_k)
        context_chunks = [r.chunk for r in reranked]

        return self.generator.generate(question, context_chunks)
