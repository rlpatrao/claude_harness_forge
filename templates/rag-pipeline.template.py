class RerankingService:
    """Optional reranker using a cross-encoder or API-based reranking."""

    def __init__(self, method: Literal["cross-encoder", "cohere", "none"] = "none", model: str | None = None) -> None:
        self.method = method
        self.model = model
        self._ranker: Any = None

        if method == "cross-encoder":
            try:
                from sentence_transformers import CrossEncoder
            except ImportError:
                raise ImportError("Install sentence-transformers: pip install sentence-transformers")
            self._ranker = CrossEncoder(model or "cross-encoder/ms-marco-MiniLM-L-6-v2")
        elif method == "cohere":
            try:
                import cohere
            except ImportError:
                raise ImportError("Install cohere: pip install cohere")
            self._ranker = cohere.Client(os.environ.get("COHERE_API_KEY", ""))

    def rerank(self, query: str, results: list[RetrievalResult], top_k: int = 5) -> list[RetrievalResult]:
        if self.method == "none" or not results:
            return results[:top_k]

        if self.method == "cross-encoder":
            pairs = [(query, r.chunk.text) for r in results]
            scores = self._ranker.predict(pairs)
            for result, score in zip(results, scores):
                result.score = float(score)
            results.sort(key=lambda r: r.score, reverse=True)
            return results[:top_k]

        if self.method == "cohere":
            response = self._ranker.rerank(
                query=query,
                documents=[r.chunk.text for r in results],
                top_n=top_k,
                model=self.model or "rerank-english-v3.0",
            )
            reranked = []
            for hit in response.results:
                result = results[hit.index]
                result.score = hit.relevance_score
                reranked.append(result)
            return reranked

        return results[:top_k]


