class GenerationService:
    """Generate answers using an LLM with retrieved context."""

    SYSTEM_PROMPT = (
        "You are a helpful assistant. Answer the user's question based on the "
        "provided context. If the context does not contain enough information, "
        "say so. Cite the relevant source documents when possible."
    )

    def __init__(self, model: str = LLM_MODEL) -> None:
        self.model = model

    def generate(self, query: str, context_chunks: list[Chunk]) -> GenerationResult:
        context_text = "\n\n---\n\n".join(
            f"[Source: {c.metadata.get('source', 'unknown')}]\n{c.text}"
            for c in context_chunks
        )

        messages = [
            {"role": "system", "content": self.SYSTEM_PROMPT},
            {
                "role": "user",
                "content": f"Context:\n{context_text}\n\nQuestion: {query}",
            },
        ]

        # Supports OpenAI-compatible APIs (OpenAI, Azure, local vLLM, etc.)
        try:
            from openai import OpenAI
        except ImportError:
            raise ImportError("Install openai: pip install openai")

        client = OpenAI()
        response = client.chat.completions.create(
            model=self.model,
            messages=messages,
            temperature=0.1,
            max_tokens=2048,
        )

        usage = {}
        if response.usage:
            usage = {
                "prompt_tokens": response.usage.prompt_tokens,
                "completion_tokens": response.usage.completion_tokens,
                "total_tokens": response.usage.total_tokens,
            }

        return GenerationResult(
            answer=response.choices[0].message.content or "",
            source_chunks=context_chunks,
            model=self.model,
            usage=usage,
        )


