class EmbeddingProvider(Protocol):
    def embed(self, texts: list[str]) -> list[list[float]]:
        ...


class OpenAIEmbeddings:
    """Embedding via OpenAI API (text-embedding-3-small, ada-002, etc.)."""

    def __init__(self, model: str = EMBEDDING_MODEL) -> None:
        try:
            from openai import OpenAI
        except ImportError:
            raise ImportError("Install openai: pip install openai")
        self.client = OpenAI()
        self.model = model

    def embed(self, texts: list[str]) -> list[list[float]]:
        response = self.client.embeddings.create(input=texts, model=self.model)
        return [item.embedding for item in response.data]


class SentenceTransformerEmbeddings:
    """Local embedding via sentence-transformers."""

    def __init__(self, model: str = EMBEDDING_MODEL) -> None:
        try:
            from sentence_transformers import SentenceTransformer
        except ImportError:
            raise ImportError("Install sentence-transformers: pip install sentence-transformers")
        self.model = SentenceTransformer(model)

    def embed(self, texts: list[str]) -> list[list[float]]:
        embeddings = self.model.encode(texts, show_progress_bar=False)
        return embeddings.tolist()


class EmbeddingService:
    """Facade for embedding with configurable provider."""

    def __init__(self, provider: Literal["openai", "local"] = "openai") -> None:
        self.provider: EmbeddingProvider
        if provider == "local":
            self.provider = SentenceTransformerEmbeddings()
        else:
            self.provider = OpenAIEmbeddings()

    def embed_texts(self, texts: list[str], batch_size: int = 64) -> list[list[float]]:
        all_embeddings: list[list[float]] = []
        for i in range(0, len(texts), batch_size):
            batch = texts[i : i + batch_size]
            all_embeddings.extend(self.provider.embed(batch))
        return all_embeddings

    def embed_chunks(self, chunks: list[Chunk], batch_size: int = 64) -> list[list[float]]:
        return self.embed_texts([c.text for c in chunks], batch_size=batch_size)


