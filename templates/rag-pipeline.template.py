class VectorStoreClient(ABC):
    """Abstract vector store interface."""

    @abstractmethod
    def upsert(self, ids: list[str], embeddings: list[list[float]], metadatas: list[dict[str, Any]], texts: list[str]) -> None:
        ...

    @abstractmethod
    def query(self, embedding: list[float], top_k: int = 10) -> list[RetrievalResult]:
        ...

    @abstractmethod
    def delete(self, ids: list[str]) -> None:
        ...


class PgVectorClient(VectorStoreClient):
    """pgvector-backed vector store using psycopg."""

    def __init__(self, connection_string: str | None = None, table: str = "embeddings") -> None:
        try:
            import psycopg
            from pgvector.psycopg import register_vector
        except ImportError:
            raise ImportError("Install pgvector and psycopg: pip install pgvector psycopg[binary]")

        self.conn_str = connection_string or os.environ.get(
            "DATABASE_URL", "postgresql://localhost:5432/rag"
        )
        self.table = table
        self._conn = psycopg.connect(self.conn_str)
        register_vector(self._conn)
        self._ensure_table()

    def _ensure_table(self) -> None:
        with self._conn.cursor() as cur:
            cur.execute("CREATE EXTENSION IF NOT EXISTS vector")
            cur.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {self.table} (
                    id TEXT PRIMARY KEY,
                    embedding vector,
                    metadata JSONB,
                    content TEXT
                )
                """
            )
            cur.execute(
                f"""
                CREATE INDEX IF NOT EXISTS {self.table}_embedding_idx
                ON {self.table} USING ivfflat (embedding vector_cosine_ops)
                WITH (lists = 100)
                """
            )
        self._conn.commit()

    def upsert(self, ids: list[str], embeddings: list[list[float]], metadatas: list[dict[str, Any]], texts: list[str]) -> None:
        with self._conn.cursor() as cur:
            for id_, emb, meta, text in zip(ids, embeddings, metadatas, texts):
                cur.execute(
                    f"""
                    INSERT INTO {self.table} (id, embedding, metadata, content)
                    VALUES (%s, %s, %s, %s)
                    ON CONFLICT (id) DO UPDATE SET embedding = EXCLUDED.embedding,
                        metadata = EXCLUDED.metadata, content = EXCLUDED.content
                    """,
                    (id_, emb, json.dumps(meta), text),
                )
        self._conn.commit()

    def query(self, embedding: list[float], top_k: int = 10) -> list[RetrievalResult]:
        with self._conn.cursor() as cur:
            cur.execute(
                f"""
                SELECT id, content, metadata, 1 - (embedding <=> %s::vector) AS score
                FROM {self.table}
                ORDER BY embedding <=> %s::vector
                LIMIT %s
                """,
                (embedding, embedding, top_k),
            )
            results = []
            for row in cur.fetchall():
                chunk = Chunk(
                    text=row[1],
                    doc_id=json.loads(row[2]).get("doc_id", ""),
                    chunk_index=0,
                    metadata=json.loads(row[2]),
                )
                results.append(RetrievalResult(chunk=chunk, score=row[3], source="vector"))
            return results

    def delete(self, ids: list[str]) -> None:
        with self._conn.cursor() as cur:
            cur.execute(f"DELETE FROM {self.table} WHERE id = ANY(%s)", (ids,))
        self._conn.commit()


class ChromaDBClient(VectorStoreClient):
    """ChromaDB-backed vector store."""

    def __init__(self, collection_name: str = "rag_collection", persist_dir: str = "./chroma_data") -> None:
        try:
            import chromadb
        except ImportError:
            raise ImportError("Install chromadb: pip install chromadb")

        self.client = chromadb.PersistentClient(path=persist_dir)
        self.collection = self.client.get_or_create_collection(
            name=collection_name,
            metadata={"hnsw:space": "cosine"},
        )

    def upsert(self, ids: list[str], embeddings: list[list[float]], metadatas: list[dict[str, Any]], texts: list[str]) -> None:
        self.collection.upsert(ids=ids, embeddings=embeddings, metadatas=metadatas, documents=texts)

    def query(self, embedding: list[float], top_k: int = 10) -> list[RetrievalResult]:
        results = self.collection.query(query_embeddings=[embedding], n_results=top_k, include=["documents", "metadatas", "distances"])
        hits: list[RetrievalResult] = []
        for i in range(len(results["ids"][0])):
            chunk = Chunk(
                text=results["documents"][0][i],
                doc_id=results["metadatas"][0][i].get("doc_id", ""),
                chunk_index=0,
                metadata=results["metadatas"][0][i],
            )
            # ChromaDB returns distances; convert to similarity for cosine
            score = 1.0 - results["distances"][0][i]
            hits.append(RetrievalResult(chunk=chunk, score=score, source="vector"))
        return hits

    def delete(self, ids: list[str]) -> None:
        self.collection.delete(ids=ids)


def create_vector_store(backend: str = VECTOR_DB, **kwargs: Any) -> VectorStoreClient:
    """Factory for vector store clients."""
    if backend == "pgvector":
        return PgVectorClient(**kwargs)
    elif backend == "chromadb":
        return ChromaDBClient(**kwargs)
    else:
        raise ValueError(f"Unsupported vector store backend: {backend}")


