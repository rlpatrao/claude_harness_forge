class RetrievalService:
    """Hybrid search combining vector similarity with keyword matching."""

    def __init__(
        self,
        vector_store: VectorStoreClient,
        embedding_service: EmbeddingService,
        keyword_weight: float = 0.3,
        vector_weight: float = 0.7,
    ) -> None:
        self.vector_store = vector_store
        self.embedding_service = embedding_service
        self.keyword_weight = keyword_weight
        self.vector_weight = vector_weight

    def search(self, query: str, top_k: int = 10) -> list[RetrievalResult]:
        """Perform hybrid search: vector similarity + BM25-style keyword scoring."""
        # Vector search
        query_embedding = self.embedding_service.embed_texts([query])[0]
        vector_results = self.vector_store.query(query_embedding, top_k=top_k * 2)

        # Keyword scoring on vector results (lightweight BM25 approximation)
        query_terms = set(query.lower().split())
        scored: list[RetrievalResult] = []

        for result in vector_results:
            chunk_terms = set(result.chunk.text.lower().split())
            overlap = len(query_terms & chunk_terms)
            keyword_score = overlap / max(len(query_terms), 1)

            combined_score = (
                self.vector_weight * result.score + self.keyword_weight * keyword_score
            )
            scored.append(
                RetrievalResult(
                    chunk=result.chunk, score=combined_score, source="hybrid"
                )
            )

        scored.sort(key=lambda r: r.score, reverse=True)
        return scored[:top_k]


