class DocumentLoader:
    """Load documents from PDF, Markdown, and plain text files."""

    _SUPPORTED_EXTENSIONS = {".pdf", ".md", ".txt", ".text", ".markdown"}

    def load(self, path: str | Path) -> list[Document]:
        path = Path(path)
        if path.is_dir():
            return self._load_directory(path)
        return [self._load_file(path)]

    def _load_directory(self, directory: Path) -> list[Document]:
        docs: list[Document] = []
        for p in sorted(directory.rglob("*")):
            if p.suffix.lower() in self._SUPPORTED_EXTENSIONS:
                try:
                    docs.append(self._load_file(p))
                except Exception:
                    logger.warning("Failed to load %s", p, exc_info=True)
        return docs

    def _load_file(self, path: Path) -> Document:
        ext = path.suffix.lower()
        metadata = {"source": str(path), "filename": path.name}

        if ext == ".pdf":
            content = self._load_pdf(path)
        else:
            content = path.read_text(encoding="utf-8")

        return Document(content=content, metadata=metadata)

    @staticmethod
    def _load_pdf(path: Path) -> str:
        try:
            import pymupdf  # PyMuPDF

            doc = pymupdf.open(str(path))
            pages = [page.get_text() for page in doc]
            doc.close()
            return "\n\n".join(pages)
        except ImportError:
            raise ImportError(
                "PyMuPDF is required for PDF loading. Install with: pip install pymupdf"
            )


