class ChunkingStrategy(ABC):
    """Base class for chunking strategies."""

    @abstractmethod
    def chunk(self, document: Document) -> list[Chunk]:
        ...


class FixedSizeChunker(ChunkingStrategy):
    """Split text into fixed-size chunks with overlap."""

    def __init__(
        self,
        chunk_size: int = CHUNK_SIZE,
        chunk_overlap: int = CHUNK_OVERLAP,
    ) -> None:
        if chunk_overlap >= chunk_size:
            raise ValueError("chunk_overlap must be less than chunk_size")
        self.chunk_size = chunk_size
        self.chunk_overlap = chunk_overlap

    def chunk(self, document: Document) -> list[Chunk]:
        text = document.content
        chunks: list[Chunk] = []
        start = 0
        index = 0

        while start < len(text):
            end = start + self.chunk_size

            # Try to break at a sentence or paragraph boundary
            if end < len(text):
                boundary = self._find_boundary(text, start, end)
                if boundary > start:
                    end = boundary

            chunk_text = text[start:end].strip()
            if chunk_text:
                chunks.append(
                    Chunk(
                        text=chunk_text,
                        doc_id=document.doc_id,
                        chunk_index=index,
                        metadata={**document.metadata, "char_start": start, "char_end": end},
                    )
                )
                index += 1

            start = end - self.chunk_overlap
            if start <= (end - self.chunk_size):
                # Prevent infinite loop on degenerate input
                start = end

        return chunks

    @staticmethod
    def _find_boundary(text: str, start: int, end: int) -> int:
        """Walk backwards from end to find a paragraph or sentence break."""
        search_region = text[max(start, end - 200) : end]
        # Prefer paragraph break
        para = search_region.rfind("\n\n")
        if para != -1:
            return max(start, end - 200) + para + 2
        # Fall back to sentence break
        sentence = max(search_region.rfind(". "), search_region.rfind(".\n"))
        if sentence != -1:
            return max(start, end - 200) + sentence + 2
        return end


class SemanticChunker(ChunkingStrategy):
    """Split text at semantic boundaries (headings, paragraph breaks).

    Falls back to fixed-size splitting for blocks that exceed max_chunk_size.
    """

    def __init__(self, max_chunk_size: int = CHUNK_SIZE) -> None:
        self.max_chunk_size = max_chunk_size
        self._fallback = FixedSizeChunker(chunk_size=max_chunk_size, chunk_overlap=CHUNK_OVERLAP)

    def chunk(self, document: Document) -> list[Chunk]:
        # Split on markdown headings and double newlines
        sections = re.split(r"(?=^#{1,3}\s)|\n{2,}", document.content, flags=re.MULTILINE)
        chunks: list[Chunk] = []
        index = 0

        for section in sections:
            section = section.strip()
            if not section:
                continue

            if len(section) <= self.max_chunk_size:
                chunks.append(
                    Chunk(
                        text=section,
                        doc_id=document.doc_id,
                        chunk_index=index,
                        metadata=document.metadata,
                    )
                )
                index += 1
            else:
                # Oversized section: use fixed-size chunker
                temp_doc = Document(content=section, metadata=document.metadata, doc_id=document.doc_id)
                for sub in self._fallback.chunk(temp_doc):
                    sub.chunk_index = index
                    chunks.append(sub)
                    index += 1

        return chunks


class ChunkingService:
    """Facade for chunking with configurable strategy."""

    def __init__(self, strategy: Literal["fixed", "semantic"] = "fixed") -> None:
        self.strategy: ChunkingStrategy
        if strategy == "semantic":
            self.strategy = SemanticChunker()
        else:
            self.strategy = FixedSizeChunker()

    def chunk_documents(self, documents: list[Document]) -> list[Chunk]:
        all_chunks: list[Chunk] = []
        for doc in documents:
            all_chunks.extend(self.strategy.chunk(doc))
        logger.info("Chunked %d documents into %d chunks", len(documents), len(all_chunks))
        return all_chunks


