class EvaluationMetrics:
    """Lightweight helpers for RAG evaluation."""

    @staticmethod
    def retrieval_precision(retrieved: list[Chunk], relevant_ids: set[str]) -> float:
        """Fraction of retrieved chunks that are relevant."""
        if not retrieved:
            return 0.0
        hits = sum(1 for c in retrieved if c.chunk_id in relevant_ids)
        return hits / len(retrieved)

    @staticmethod
    def retrieval_recall(retrieved: list[Chunk], relevant_ids: set[str]) -> float:
        """Fraction of relevant chunks that were retrieved."""
        if not relevant_ids:
            return 0.0
        hits = sum(1 for c in retrieved if c.chunk_id in relevant_ids)
        return hits / len(relevant_ids)

    @staticmethod
    def answer_faithfulness(answer: str, context_chunks: list[Chunk]) -> dict[str, Any]:
        """Basic faithfulness check: how many sentences in the answer can be
        attributed to the provided context. Returns a dict with score and details.

        For production use, replace with a model-graded evaluation (e.g. RAGAS).
        """
        context_text = " ".join(c.text.lower() for c in context_chunks)
        sentences = [s.strip() for s in re.split(r"[.!?]+", answer) if s.strip()]

        if not sentences:
            return {"score": 1.0, "total_sentences": 0, "grounded_sentences": 0}

        grounded = 0
        for sentence in sentences:
            words = sentence.lower().split()
            if len(words) < 3:
                grounded += 1
                continue
            # Check if a meaningful fraction of words appear in context
            overlap = sum(1 for w in words if w in context_text)
            if overlap / len(words) >= 0.5:
                grounded += 1

        return {
            "score": grounded / len(sentences),
            "total_sentences": len(sentences),
            "grounded_sentences": grounded,
        }

    @staticmethod
    def mean_reciprocal_rank(retrieved: list[Chunk], relevant_ids: set[str]) -> float:
        """Mean Reciprocal Rank of the first relevant result."""
        for i, chunk in enumerate(retrieved, start=1):
            if chunk.chunk_id in relevant_ids:
                return 1.0 / i
        return 0.0


