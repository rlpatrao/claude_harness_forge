# Shared retry policy for activities
_default_retry = RetryPolicy(
    initial_interval=timedelta(seconds=1),
    backoff_coefficient=2.0,
    maximum_interval=timedelta(minutes=5),
    maximum_attempts=5,
    non_retryable_error_types=["ValueError"],
)


@workflow.defn(name=WORKFLOW_NAME)
class ProcessingWorkflow:
    """Main workflow with saga compensation and human-in-the-loop approval.

    Flow:
        1. validate_input
        2. process_step_one
        3. Wait for human approval (signal)
        4. process_step_two
        5. finalize

    On failure at any step, previously completed activities are compensated
    in reverse order (saga pattern).
    """

    def __init__(self) -> None:
        self._approval_decision: ApprovalDecision | None = None
        self._compensation_stack: list[CompensationRecord] = []

    # --- Signals --------------------------------------------------------

    @workflow.signal(name="approval_decision")
    async def handle_approval(self, decision: str) -> None:
        """Signal handler for human-in-the-loop approval.

        Send this signal from an external system (UI, Slack bot, CLI) to
        unblock the workflow.
        """
        workflow.logger.info("Received approval decision: %s", decision)
        self._approval_decision = ApprovalDecision(decision)

    # --- Queries --------------------------------------------------------

    @workflow.query(name="get_status")
    def get_status(self) -> dict[str, Any]:
        """Query the current workflow state."""
        return {
            "approval_decision": self._approval_decision.value if self._approval_decision else "pending",
            "compensation_stack_depth": len(self._compensation_stack),
        }

    # --- Main workflow logic -------------------------------------------

    @workflow.run
    async def run(self, input: WorkflowInput) -> WorkflowOutput:
        """Execute the workflow with saga compensation."""
        request_id = input.request_id
        workflow.logger.info("Starting %s for request %s", WORKFLOW_NAME, request_id)

        base_data = {"request_id": request_id, **input.payload}

        try:
            # Step 1: Validate
            await workflow.execute_activity(
                validate_input,
                base_data,
                start_to_close_timeout=timedelta(seconds=30),
                retry_policy=_default_retry,
            )

            # Step 2: Process step one
            step_one_result = await workflow.execute_activity(
                process_step_one,
                base_data,
                start_to_close_timeout=timedelta(minutes=5),
                retry_policy=_default_retry,
            )
            self._compensation_stack.append(
                CompensationRecord(
                    activity_name="process_step_one",
                    compensation_name="compensate_step_one",
                    input_data=base_data,
                )
            )

            # Step 3: Human-in-the-loop — wait for approval signal
            await self._wait_for_approval(request_id)

            if self._approval_decision == ApprovalDecision.REJECTED:
                workflow.logger.info("Workflow rejected by approver, compensating")
                await self._compensate()
                return WorkflowOutput(
                    request_id=request_id,
                    status="rejected",
                    compensated=True,
                )

            # Step 4: Process step two
            step_two_result = await workflow.execute_activity(
                process_step_two,
                {**base_data, "step_one": step_one_result},
                start_to_close_timeout=timedelta(minutes=5),
                retry_policy=_default_retry,
            )
            self._compensation_stack.append(
                CompensationRecord(
                    activity_name="process_step_two",
                    compensation_name="compensate_step_two",
                    input_data=base_data,
                )
            )

            # Step 5: Finalize
            final_result = await workflow.execute_activity(
                finalize,
                {**base_data, "step_one": step_one_result, "step_two": step_two_result},
                start_to_close_timeout=timedelta(minutes=2),
                retry_policy=_default_retry,
            )

            # Success notification
            await workflow.execute_activity(
                send_notification,
                {"request_id": request_id, "type": "success", "details": final_result},
                start_to_close_timeout=timedelta(seconds=30),
                retry_policy=_default_retry,
            )

            return WorkflowOutput(
                request_id=request_id,
                status="completed",
                results=final_result,
            )

        except Exception as exc:
            workflow.logger.error("Workflow failed: %s — running compensation", exc)

            # Notify about failure
            await workflow.execute_activity(
                send_notification,
                {"request_id": request_id, "type": "failure", "error": str(exc)},
                start_to_close_timeout=timedelta(seconds=30),
                retry_policy=_default_retry,
            )

            # Saga: compensate in reverse order
            await self._compensate()

            return WorkflowOutput(
                request_id=request_id,
                status="failed",
                results={"error": str(exc)},
                compensated=True,
            )

    async def _wait_for_approval(self, request_id: str) -> None:
        """Block until an approval signal arrives or timeout is reached."""
        # Notify that approval is needed
        await workflow.execute_activity(
            send_notification,
            {"request_id": request_id, "type": "approval_required"},
            start_to_close_timeout=timedelta(seconds=30),
            retry_policy=_default_retry,
        )

        # Wait with a timeout — adjust for your SLA
        approval_timeout = timedelta(hours=24)
        try:
            await workflow.wait_condition(
                lambda: self._approval_decision is not None,
                timeout=approval_timeout,
            )
        except asyncio.TimeoutError:
            workflow.logger.warning("Approval timed out after %s", approval_timeout)
            raise TimeoutError(
                f"Human approval not received within {approval_timeout} for {request_id}"
            )

    async def _compensate(self) -> None:
        """Run compensation activities in reverse order (saga pattern)."""
        while self._compensation_stack:
            record = self._compensation_stack.pop()
            workflow.logger.info("Compensating: %s", record.compensation_name)
            try:
                compensation_fn = _COMPENSATION_REGISTRY[record.compensation_name]
                await workflow.execute_activity(
                    compensation_fn,
                    record.input_data,
                    start_to_close_timeout=timedelta(minutes=5),
                    retry_policy=RetryPolicy(
                        initial_interval=timedelta(seconds=2),
                        backoff_coefficient=2.0,
                        maximum_interval=timedelta(minutes=10),
                        maximum_attempts=10,
                    ),
                )
            except Exception:
                workflow.logger.error(
                    "Compensation failed for %s — manual intervention required",
                    record.compensation_name,
                    exc_info=True,
                )


