async def start_workflow(
    input: WorkflowInput,
    temporal_address: str = "localhost:7233",
    namespace: str = NAMESPACE,
) -> str:
    """Start a new workflow execution. Returns the workflow run ID."""
    client = await Client.connect(temporal_address, namespace=namespace)

    handle = await client.start_workflow(
        ProcessingWorkflow.run,
        input,
        id=f"{WORKFLOW_NAME}-{input.request_id}",
        task_queue=TASK_QUEUE,
    )

    logger.info("Started workflow %s (run_id=%s)", handle.id, handle.result_run_id)
    return handle.id


async def send_approval(
    workflow_id: str,
    decision: ApprovalDecision,
    temporal_address: str = "localhost:7233",
    namespace: str = NAMESPACE,
) -> None:
    """Send an approval signal to a running workflow."""
    client = await Client.connect(temporal_address, namespace=namespace)
    handle = client.get_workflow_handle(workflow_id)
    await handle.signal(ProcessingWorkflow.handle_approval, decision.value)
    logger.info("Sent %s signal to %s", decision.value, workflow_id)


